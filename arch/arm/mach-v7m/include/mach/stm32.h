#ifndef _ASM_ARCH_STM32F_H
#define _ASM_ARCH_STM32F_H

#define STM32_PERIPH_BASE	0x40000000UL

#define STM32_APB2_PERIPH_BASE	(STM32_PERIPH_BASE + 0x00010000)
#define STM32_AHB1_PERIPH_BASE	(STM32_PERIPH_BASE + 0x00020000)

#define STM32_SYSCFG_BASE	(STM32_APB2_PERIPH_BASE + 0x3800)
#define STM32_FLASH_CNTL_BASE	(STM32_AHB1_PERIPH_BASE + 0x3C00)

#define STM32_RCC_BASE		IOMEM(STM32_AHB1_PERIPH_BASE + 0x3800)
#define STM32_USART1_BASE	IOMEM(STM32_APB2_PERIPH_BASE + 0x1000)
#define STM32_GPIO_BASE(i)	IOMEM(STM32_AHB1_PERIPH_BASE + ((i) * 0x400))

#define STM32F_FLASH_BASE	0x08000000
#define STM32F_SRAM_BASE	0x20000000
#define STM32F_PWRCFG_BASE	0x40007000
#define STM32F_SYSCFG_BASE	0x40013800
#define STM32F_FMC_BASE		0xA0000000

#define STM32F_SDRAM_BANK1_BASE	0xD0000000
#define STM32F_SDRAM_BANK2_BASE	0x90000000

#define STM32H_SYSCFG_BASE	0x58000400
#define STM32H_FMC_BASE		0x52004000

#endif
